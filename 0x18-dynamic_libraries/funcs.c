#include "mainop.h"

/**
 * add - Adding two integers
 * @a: input integer to sum
 * @b: input integer to sum
 * Return: The sum of the integers
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Adding two integers
 * @a: input integer
 * @b: input integer
 * Return: The difference of the integers
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Adding two integers
 * @a: input integer to multiply
 * @b: input integer to multiply
 * Return: The product of the integers
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - Adding two integers
 * @a: input integer
 * @b: input integer
 * Return: The modulo of the integers
 */

int mod(int a, int b)
{
	return (a % b);
}

